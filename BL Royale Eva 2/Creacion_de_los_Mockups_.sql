- Diseño visual coherente y profesional.
- Consistencia entre pantallas.
- Usabilidad y experiencia de usuario (UX).
- Componentes reutilizables bien definidos.


Si lo deseas, también puedo generar imágenes o maquetas visuales reales a partir de este diseño. Pero primero, te detallo la estructura lógica completa y las pautas visuales para que sirva como base de diseño o desarrollo UI.
🎨 Lineamientos Generales de Diseño Visual (UI)
🧩 1. Estilo Gráfico:

- Tema visual: Futurista, competitivo, con tonos oscuros y detalles neón.
- Paleta de colores:
- Fondo principal: #121212.
- Primario: #1e88e5 (azul eléctrico).
- Secundario: #00e676 (verde neón).
- Texto: #ffffff, #cccccc.

Tipografía:

- Fuente principal: "Orbitron" (estilo gamer, sans-serif).
- Tamaños variables: 16px para cuerpo, 24px para títulos, 32px en pantallas clave.

Botones:

- Con bordes redondeados (border-radius: 12px).
- Sombras suaves para resaltar (box-shadow).
- Animaciones suaves al hacer hover (transform: scale(1.05).

🖥️ Componentes Reutilizables:

1. NavBar Íconos o enlaces a: 

- Inicio, Dashboard, Perfil, Cerrar sesión.
- Barra superior fija.
- Indicador de usuario conectado.

2. Botón Estados:

- default, hover, disabled.
- Iconos opcionales.
- Tamaños: sm, md, lg.

3. Tarjeta de información:
Usado para: estadísticas, resumen de jugador, info de combate.

- Contenedor con borde y fondo translúcido

📱 Maquetas por Pantalla (Mockups)
A continuación te describo cómo debe lucir cada vista. Si deseas, puedo convertir estos diseños en imágenes visuales.
1. 🏠 Inicio (/) Propósito:

- Entrada al juego.
- Logo grande de BL Royale (centrado).
- Dos botones prominentes:
- "Iniciar sesión":
- "Registrarse":
- Fondo animado o imagen de héroes/jugadores.
- Versión mínima visible sin scroll.

2. 🔐 Login (/login)
Propósito: Autenticación.
Título: "Bienvenido de vuelta".
Campos:

- Usuario o correo.
- Contraseña.
- Botón: "Entrar".
- Link: ¿No tienes cuenta? → /register.
- Error visible si hay intento fallido..

3. ✍️ Registro (/register)
Propósito: Crear cuenta
Campos:

- Usuario
- Correo electrónico
- Contraseña
- Confirmar contraseña
- Checkbox de términos y condiciones

Botón: "Crear cuenta"
- UX: Validación instantánea (color verde/rojo)

4. 📊 Dashboard (/dashboard)
Propósito: Vista principal.
Bienvenida al jugador (Hola, [nombre]!).

Tarjetas:
- Progreso / Nivel.
- Estadísticas (Victorias, Derrotas).

Botones grandes:
- "Buscar partida".
- "Ver perfil".

Sección de noticias o alertas del juego.

5. 🔎 Matchmaking (/matchmaking)
Propósito: Esperar partida
Indicador visual de "Buscando jugadores…"
- Animación (radar, círculos girando)
- Botón: "Cancelar búsqueda"
- Mensajes: "Esperando más jugadores", "Listo para empezar"

6. ⚔️ Game (/game)
Propósito: Juego activo
- Vista de tablero o campo de batalla

Panel lateral:
- Jugadores activos.
- Tiempo o turnos restantes.

Controles:
- Acciones posibles (ataque, defensa, usar objeto).
- Chat (opcional).

7. 👤 Perfil (/profile)
Propósito: Gestión del jugador
Avatar editable
Campos:
- Nombre del jugador.
- Biografía / lema.

- Estadísticas generales:
- Total partidas, victorias, derrotas.
- Botón: "Editar perfil".

💡 Principios UX Aplicados
Principio	Aplicación en BL Royale
1. Consistencia	Mismos estilos de botones, tipografía, colores en todas las vistas.
2. Visibilidad	Acciones claras y visibles (CTA destacados).
3. Feedback	Respuestas visuales a clics, cargas, errores, éxito.
4. Accesibilidad	Contrastes fuertes, texto legible, navegación con teclado.
5. Minimizar carga cognitiva	Navegación clara, información agrupada por contexto.

